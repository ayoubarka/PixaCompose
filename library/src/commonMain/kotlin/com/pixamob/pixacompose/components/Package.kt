package com.pixamob.pixacompose.components

/**
 * UI Components Package
 *
 * A comprehensive collection of production-ready UI components for Compose Multiplatform
 * mobile applications (Android & iOS) using Material 3 design principles.
 *
 * ## Component Categories
 *
 * ### Actions - Interactive Elements
 * User interaction components for triggering operations:
 * - **Button** - Primary interaction component with multiple variants
 * - **IconButton** - Compact icon-only buttons
 * - **FloatingActionButton** - Prominent floating actions
 * - **Tab** - Navigation between content sections
 * - **Chip** - Compact elements for tags, filters, selections
 *
 * ### Inputs - Form & Data Entry
 * Components for collecting user input:
 * - **TextField** - Single-line text input
 * - **TextArea** - Multi-line text input
 * - **SearchBar** - Specialized search input with suggestions
 * - **Checkbox** - Multi-selection control
 * - **RadioButton** - Single-selection control
 * - **Switch** - Binary toggle control
 * - **Slider** - Value/range selection
 * - **DatePicker** - Date selection (third-party wrapper)
 * - **TimePicker** - Time selection (third-party wrapper)
 * - **Dropdown** - Selection from list of options
 * - **AutoComplete** - Text input with suggestions
 *
 * ### Feedback - User Feedback
 * Components for showing status and notifications:
 * - **Toast** - Temporary notification messages
 * - **Snackbar** - Dismissible messages with actions
 * - **Alert** - Inline important messages
 * - **Badge** - Status indicators and notification counts
 * - **ProgressIndicator** - Loading and progress states
 * - **Skeleton** - Content placeholders with shimmer effect (third-party wrapper)
 * - **LoadingOverlay** - Full-screen loading state
 *
 * ### Display - Content Display
 * Components for presenting content:
 * - **Card** - Content containers
 * - **Avatar** - Profile pictures and initials
 * - **Image** - Async image loading with states
 * - **Divider** - Content separators
 * - **Tag** - Labels and categories
 * - **EmptyState** - Empty content states
 * - **List** - Scrollable list of items
 * - **Grid** - Grid layout for items
 *
 * ### Navigation - Navigation Elements
 * Components for app navigation:
 * - **BottomNavBar** - Bottom tab navigation
 * - **TopNavBar** - Top navigation bar with actions
 * - **Drawer** - Side navigation drawer
 * - **TabBar** - Horizontal tab navigation
 * - **Breadcrumb** - Hierarchical navigation trail
 * - **Stepper** - Multi-step process navigation
 *
 * ### Overlay - Modal Elements
 * Components that overlay content:
 * - **Dialog** - Modal dialogs
 * - **BottomSheet** - Bottom slide-up sheets
 * - **Popover** - Contextual popups
 * - **Tooltip** - Contextual hints
 * - **Menu** - Context menus
 *
 * ## Usage
 *
 * All components are theme-aware and consume the centralized theme system.
 * Wrap your app with `AppTheme` to enable theming:
 *
 * ```kotlin
 * @Composable
 * fun App() {
 *     AppTheme {
 *         // Your app content
 *         MyScreen()
 *     }
 * }
 * ```
 *
 * @see com.pixamob.pixacompose.theme.AppTheme
 */

